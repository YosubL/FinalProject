package com.spring.app.seungwoo.domain;

public class WeclomemailVO {
	
	private int mail_seq;    
	private String mail_name;
	private String mail_department;
	private String mail_email;
	private String mail_phonenumber;
	private String mail_senddate;
	
	public int getMail_seq() {
		return mail_seq;
	}
	
	public void setMail_seq(int mail_seq) {
		this.mail_seq = mail_seq;
	}
	
	public String getMail_name() {
		return mail_name;
	}
	
	public void setMail_name(String mail_name) {
		this.mail_name = mail_name;
	}
	
	public String getMail_department() {
		return mail_department;
	}
	
	public void setMail_department(String mail_department) {
		this.mail_department = mail_department;
	}
	
	public String getMail_email() {
		return mail_email;
	}
	
	public void setMail_email(String mail_email) {
		this.mail_email = mail_email;
	}
	
	public String getMail_phonenumber() {
		return mail_phonenumber;
	}
	
	public void setMail_phonenumber(String mail_phonenumber) {
		this.mail_phonenumber = mail_phonenumber;
	}
	
	public String getMail_senddate() {
		return mail_senddate;
	}

	public void setMail_senddate(String mail_senddate) {
		this.mail_senddate = mail_senddate;
	}


}
